    public void saveConfig(){
        int ram = jComboBox1.getSelectedIndex();
        switch (ram){
            case 0: InnerApi.configuration.setProperty("maxRAM", "512");
                break;
            case 1: InnerApi.configuration.setProperty("maxRAM", "1024");
                break;
            case 2: InnerApi.configuration.setProperty("maxRAM", "2048");
                break;
            case 3: InnerApi.configuration.setProperty("maxRAM", "3072");
                break;
            case 4: InnerApi.configuration.setProperty("maxRAM", "4096");
                break;
            case 5: InnerApi.configuration.setProperty("maxRAM", "5120");
                break;
            case 6: InnerApi.configuration.setProperty("maxRAM", "6144");
                break;
        }
        if (jCheckBox1.isSelected()){
            InnerApi.configuration.setProperty("systemProxy", "true");
        } else{
            InnerApi.configuration.setProperty("systemProxy", "false");
        }
        if (jCheckBox2.isSelected()){
            InnerApi.configuration.setProperty("defaultPath", "true");
            InnerApi.configuration.setProperty("installPath", "default");
        } else{
            InnerApi.configuration.setProperty("defaultPath", "false");
            InnerApi.configuration.setProperty("installPath", jTextField1.getText());
        }
        if (jCheckBox4.isSelected()){
            InnerApi.configuration.setProperty("consoleActivated", "false");
            InnerApi.configuration.setProperty("consoleMinimized", "false");
        } else{
            InnerApi.configuration.setProperty("consoleActivated", "true");
            if (jCheckBox3.isSelected()){
                InnerApi.configuration.setProperty("consoleMinimized", "true");
            } else{
                InnerApi.configuration.setProperty("consoleMinimized", "false");
            }
        }
        if (InnerApi.Init.mainGUI.jCheckBox1.isSelected()){
            InnerApi.configuration.setProperty("rememberUser", "true");
        } else{
            InnerApi.configuration.setProperty("rememberUser", "false");
        }
        if (InnerApi.Init.mainGUI.jCheckBox2.isSelected()){
            InnerApi.configuration.setProperty("rememberPassword", "true");
        } else{
            InnerApi.configuration.setProperty("rememberPassword", "false");
        }
        try {
            FileOutputStream out = new FileOutputStream(InnerApi.Files.config());
            InnerApi.configuration.store(out, "Options MLR");
            out.close();
        } catch (Exception e) {
            InnerApi.Init.error.setError(e);
        }
        InnerApi.Init.reloadPath();
    }
    public void loadConfig(){
        int ram = Integer.parseInt(InnerApi.configuration.getProperty("maxRAM"));
        switch (ram){
            case 512: jComboBox1.setSelectedItem("512Mb");
                break;
            case 1024: jComboBox1.setSelectedItem("1Gb");
                break;
            case 2048: jComboBox1.setSelectedItem("2Gb");
                break;
            case 3072: jComboBox1.setSelectedItem("3Gb");
                break;
            case 4096: jComboBox1.setSelectedItem("4Gb");
                break;
            case 5120: jComboBox1.setSelectedItem("5Gb");
                break;
            case 6144: jComboBox1.setSelectedItem("6Gb");
                break;
        }
        jCheckBox1.setSelected(InnerApi.configuration.getProperty("systemProxy").equals("true"));
        boolean defaul = InnerApi.configuration.getProperty("defaultPath").equals("true");
        jCheckBox2.setSelected(defaul);
        jCheckBox2ActionPerformed(null);
        if (!defaul){
            jTextField1.setText(InnerApi.configuration.getProperty("installPath"));
        }
        defaul = InnerApi.configuration.getProperty("consoleActivated").equals("true");
        if (!defaul){
            jCheckBox4.setSelected(true);
        } else{
            jCheckBox4.setSelected(false);
            if (InnerApi.configuration.getProperty("consoleMinimized").equals("true")){
                jCheckBox3.setSelected(true);
            } else{
                jCheckBox3.setSelected(false);
            }
        }
        jCheckBox4ActionPerformed(null);
    }
