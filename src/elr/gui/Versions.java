    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        installing = true;
        System.out.println("Please wait, this may take a while...");
        jLabel5.setText("Starting protocols");
        jButton1.setEnabled(false);
        jButton1.setText("Installing");
        try {
            if (jTabbedPane1.getSelectedIndex() == 0){
                if (versions.containsKey((String) jList1.getSelectedValue())){
                    helper.download(versions.get((String) jList1.getSelectedValue()), jProgressBar1, 
                            jLabel5, false);
                }
            } else if (jTabbedPane1.getSelectedIndex() == 1){
                if (modPacks.containsKey((String) jList2.getSelectedValue())){
                    helper.download(modPacks.get((String) jList2.getSelectedValue()), jProgressBar1,
                            jLabel5, true);
                }
            }
        } catch (Exception e) {
            ExceptionControl.showException(3, e, "Failed to install " + (jTabbedPane1.getSelectedIndex() 
                    == 1 ? "ModPack" : "Minecraft"));
            reload(helper);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

