    /**
     * Checks if a version is deprecated.
     * @param version The version to check.
     * @return {@code true} if the version is deprecated, {@code false} in otherwise.
     */
    public boolean isDeprecated(String version){ return (actual.equals(version) && !isRecent(version)); }
    
    /**
     * Checks if a version is recent.
     * @param version The version to check.
     * @return {@code true} if the version is recent, {@code false} in otherwise.
     */
    public boolean isRecent(String version){
        return recent.equals(version.substring(0, version.lastIndexOf(".")));
    }
    
    /**
     * Method used to close the GUI when the download finished.
     * @param obj The object where is called.
     */
    public void finalized(Object obj){
        if (obj instanceof VersionsClass){
            if (obj.equals(helper)){
                installing = false;
                ((VersionsClass)obj).close();
            }
        }
    }
    
    /**
     * Reinitializes the GUI.
     * @param obj The object where is called.
     */
    public void reload(Object obj){
        if (obj instanceof VersionsClass){
            if (obj.equals(helper)){
                installing = false;
                jLabel5.setText("");
                jButton1.setEnabled(true);
                jButton1.setText("Install");
                jProgressBar1.setVisible(false);
                jProgressBar1.setValue(0);
                jProgressBar1.setString("");
                jProgressBar1.setEnabled(false);
            }
        }
    }
    
    /**
     * Initializes the model of the two lists.
     */
    private void initModel(){
        System.out.println("Initializing model...");
        versions = helper.checkVersions();
        modPacks = helper.checkModPacks();
        if (versions.containsKey("No versions founded")){
            actual = versions.firstKey();
            recent = "";
        } else{
            actual = versions.lastKey().split(" ")[2];
            recent = helper.obtainRecentVersion(versions);
        }
        DefaultListModel modelo = helper.getModel(versions);
        DefaultListModel model = helper.getModel(modPacks);
        jList1.setModel(modelo);
        jList2.setModel(model);
    }

